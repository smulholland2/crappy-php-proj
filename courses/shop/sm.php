For all courses you will need an email address. This can be the employee's email or the store manager's email. For Food Handler Training and Allergen Training you will also need to know the employee's birthday. Once you have enrolled the employee into the training, give the employee the user name and password you created for them, and direct them to www.tapseries.com to click Login to Course.
Food Safety Managers have four weeks to complete the training.<br>
Food Handlers and Allergen Awareness trainees have one week to complete the training.<br><br>

You will be able to enroll into most courses without contacting the DM. A few food handler jurisdictions require fees to be paid prior to enrolling employees. If your store is in one of these jurisdictions the site will indicate the course does not have licenses available, you need to contact your District Manager, tell the DM how many enrollment rights you need for the course. Once the DM has paid the fees enrollment rights will be available for you to add employees.
