Schedule Out-of-Store Test Center Examination Appointment<br>
If the examination is not being proctored in-store, tell your DM you have an employee needing to take the Food Safety Manager Certification Examination. The DM or DMA will register the employee to take the examination at a test center. To register, the DM will need to have the employee on the phone to provide information. Work with your employee and the DM to schedule a time for them to be on the phone together while the DM registers the employee. Once registered, the DM will forward instructions to the employee to schedule an examination appointment. The examination is non-refundable and expires in six (6) months.<br><br>

