                        <h5>www.tapseries.com "cannot display the webpage".</h5>
                        <div class="well">
                        <p>To solve this problem, you can try changing the settings of your internet connection:</p>
                        <ol>
                            <li>Go the Control Panel.</li>
                            <li>Click Network and Internet, then Network and Sharing Center, and click Change adapter settings.</li>
                            <li>Select the connection for which you want to configure. For example:</li>
                                <ul>
                                    <li>To change the settings for an Ethernet connection, right-click Local Area Connection, and click Properties.</li>
                                    <li>To change the settings for a wireless connection, right-click Wireless Network Connection, and click Properties.</li>
                                    <li>If you are prompted for an administrator password or confirmation, type the password or provide confirmation.</li>
                                </ul>
                            <li>Select the Networking tab. Under This connection uses the following items, select Internet Protocol Version 4 (TCP/IPv4) or Internet Protocol Version 6 (TCP/IPv6) and then click Properties.</li>
                            <li>Click Advanced and select the DNS tab. If there are any DNS server IP addresses listed there, write them down for future reference, and remove them from this window.</li>
                            <li>Click OK.</li>
                            <li>Select Use the following DNS server addresses. If there are any IP addresses listed in the Preferred DNS server or Alternate DNS server, write them down for future reference.</li>
                            <li>Replace those addresses with the IP addresses listed below:</li>
                                <ul>
                                    <li>For IPv4: enter 8.8.8.8 for Preferred and 8.8.4.4 for Alternate.</li>
                                    <li>Alternate IPv4: enter 208.67.220.220 for Preferred and 208.67.222.222 for Alternate.</li>
                                    <li>For IPv6: enter 2001:4860:4860::8888 for Preferred and 2001:4860:4860::8844 for Alternate.</li>
                                </ul>
                            <li>Restart the computer and try the website again.</li>
