<b>What is the Revocation Policy and Procedure?</b><br>
The  food handler is responsible for taking the food handler course and test unless exempted under state Food Handler Card Law. The food handler card is issued to the individual food handler taking the course and passing the exam. The course and examination are not proctored.
TAP Series reserves the right to revoke a Certificate of Achievement and  Food Handler card based on a security violation. Any student enrolled in the course attests that they are that person taking the course and examination. To fraudulently or deceptively register or attempt to register as a student in the course to obtain the Certificate of Achievement and  Food Handler card is a security violation.
